# The reversed function takes a list and returns a new list with the elements in reverse order.
#
# Arguments:
#	list (list): a list of elements
#
# Returns:
#	list: a new list with the elements in reverse order
#
# Example:
#	list = [1, 2, 3]
#	reversed_list = reversed(list)
#	# [3, 2, 1]
def reversed(list):
	return list[::-1]
